namespace Pilgrimage;

/// <summary>
/// Represents an item rewarded for completing a quest.
/// </summary>
public class QuestRewardItem
{
    /// <summary>
    /// Gets or sets the unique identifier for the reward item.
    /// </summary>
    public int Id { get; set; }

    /// <summary>
    /// Gets or sets the count of the reward item.
    /// </summary>
    public int Count { get; set; }
}
